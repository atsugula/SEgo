    public void exportar(){
        JFileChooser archivo = new JFileChooser();
        archivo.setDialogTitle("Seleccionar carpeta");
        archivo.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int scriptGuardado = archivo.showSaveDialog(null);
        if(scriptGuardado == JFileChooser.APPROVE_OPTION){
            String ruta = archivo.getSelectedFile().getPath();
            txtRuta.setText(ruta);
        }
    }
    
