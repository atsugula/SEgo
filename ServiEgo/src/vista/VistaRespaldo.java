    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String ruta = txtRuta.getText();
        String scriptSQL = "\\Respaldo-BD-Servi-Repuestos-EGO.sql";
        String lugarMysql;
        try {
            if(ruta.trim().length()>0){
                lugarMysql = "C:\\xampp\\mysql\\bin\\mysqldump --routines -ujorge -pjorge -B ego -r "+ruta+scriptSQL;
                Runtime rtime = Runtime.getRuntime();
                rtime.exec(lugarMysql);
                iconoMostrar = vistaImg.iconoExcepcion("success");
                JOptionPane.showMessageDialog(null, "El respaldo se ha generado con exitó", "ERROR", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);
            }else{
                throw new Exception("Seleccione una ruta para generar el respaldo");
            }
        } catch (Exception e) {
            iconoMostrar = vistaImg.iconoExcepcion("error");
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);
        }
        
                
    }//GEN-LAST:event_jButton1ActionPerformed

