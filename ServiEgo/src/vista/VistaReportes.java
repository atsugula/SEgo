    public void generarReporteVenta(String fecha1, String fecha2){
    
        Map<String,Object> parametros = new HashMap<String,Object>();
        parametros.put("fecha1", fecha1);
        parametros.put("fecha2", fecha2);
         if (Persistencia.conectar()) {
            try {
                JasperReport jr = (JasperReport) JRLoader.loadObjectFromFile("C:\\Users\\usuario\\Documents\\impre\\ServiEgo\\src\\reportes\\reportesVentas.jasper");
                JasperPrint jasperPrint = JasperFillManager.fillReport(jr, parametros, Persistencia.con);
                JasperViewer jv = new JasperViewer(jasperPrint, false);
                jv.setVisible(true);
                jv.setAlwaysOnTop(true);
                jv.setTitle("REPORTE DE VENTAS");
            } catch (JRException ex) {
                Logger.getLogger(VistaReportes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
