    private void btnVentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVentasActionPerformed
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        if(DateFecha1.getDate() == null || DateFecha2.getDate() == null){
            iconoMostrar = vistaImg.iconoExcepcion("error");
            JOptionPane.showMessageDialog(null, "Las fechas no pueden ir vacias", "ERROR", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);   
        }else{
            String Primerafecha = sdf.format(DateFecha1.getDate());
            String Seguntafecha = sdf.format(DateFecha2.getDate());
            generarReporteVenta(Primerafecha, Seguntafecha);
        }        
        
    }//GEN-LAST:event_btnVentasActionPerformed

