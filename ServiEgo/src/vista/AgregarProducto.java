    private void jTableTablaProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTablaProductoMouseClicked
        
        if (evt.getClickCount() == 2) {
            String dato = JOptionPane.showInputDialog("¿Qué cantidad desea comprar?");
            if (dato != null) {
                if (!dato.equals("")) {
                    int cant = Integer.parseInt(dato);
                    if (cant > 0) {
                        int fila = jTableTablaProducto.getSelectedRow();
                        int codigo = Integer.parseInt(jTableTablaProducto.getValueAt(fila, 0).toString());
                        int stock = Integer.parseInt(jTableTablaProducto.getValueAt(fila, 3).toString());                        
                        Producto productoComprar = controladorProductoAgregar.findProducto(codigo);
                        if (productoComprar.getUnidsProducto()>= cant) {
                            int stockResta = stock - cant;
                            jTableTablaProducto.setValueAt(stockResta, fila, 3);
                            DetalleVenta nuevoDetalle = new DetalleVenta();
                            nuevoDetalle.setCantidadDetalle(cant);
                            nuevoDetalle.setIdProducto(productoComprar);
                            try {
                                int descuento = Integer.parseInt(JOptionPane.showInputDialog("Si tiene algún descuento, Agregelo"));
                                nuevoDetalle.setDescuentoDetalle(descuento);
                            } catch (NumberFormatException e) {
                                nuevoDetalle.setDescuentoDetalle(0);

                            }
                            vistaVentas.detalleVenta.add(nuevoDetalle);
                            JOptionPane.showMessageDialog(this, "Producto agregado");
                            vistaVentas.llenarTablaproducto();

                        } else {
                            JOptionPane.showMessageDialog(this, "Producto sin Stock");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "LA CANTIDAD DEBE SER MAYOR A 0");
                    }
                }else{
                    if (dato != null) {
                        JOptionPane.showMessageDialog(this, "LA CANTIDAD NO PUEDE IR VACÍA");
                    }
                }
            }

        }
    }//GEN-LAST:event_jTableTablaProductoMouseClicked

