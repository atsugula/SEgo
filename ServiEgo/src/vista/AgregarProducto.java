     public void llenarTablaProducto(){
        
        DefaultTableModel modelo = new DefaultTableModel(){
            public boolean isCellEditable(int rowIndex, int columnIndex){
                return false;
            }
        };
        
        listaProductoAgregar = controladorProductoAgregar.consultaxStock();
        
        modelo.addColumn("id");
        modelo.addColumn("Nombre Producto");
        modelo.addColumn("Precio venta");
        modelo.addColumn("Stock");
        modelo.addColumn("Categoria");

        String[] datos = new String[5];
        for (Producto p : listaProductoAgregar) {
            datos[0] = p.getIdProducto()+"";
            datos[1] = p.getNombreProducto()+"";
            datos[2] = p.getPrecioVentaProducto()+"";
            datos[3] = p.getUnidsProducto()+"";
            if(p.getIdCategoria() != null){
                datos[4] = p.getIdCategoria().getNombreCategoria();
            }else{
                datos[4] = null;
            }   
            modelo.addRow(datos);
        }        
        jTableTablaProducto.setModel(modelo);
    }  
    
