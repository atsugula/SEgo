    private void tablaProveedoresMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProveedoresMousePressed

        int fila = tablaProveedores.getSelectedRow();
        int codigo = Integer.parseInt(tablaProveedores.getValueAt(fila, 0).toString());
        proveedorSeleccionado = controladorProveedor.findProveedor(codigo);
        if(proveedorSeleccionado == null){
            JOptionPane.showMessageDialog(this, "El proveedor no existe, por favor cierre la ventana y vuelva a abrirla.");
        }else{

            txtNit.setText(proveedorSeleccionado.getNitProveedor()+"");
            txtNombre.setText(proveedorSeleccionado.getNombreProveedor());
            txtTelefono.setText(proveedorSeleccionado.getTelefonoProveedor());
            txtCorreo.setText(proveedorSeleccionado.getCorreoProveedor());

            btnCrear.setEnabled(false);
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }//GEN-LAST:event_tablaProveedoresMousePressed

    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        
        try {
            
            Proveedor nuevoProveedor = new Proveedor();
            //Validamos si los campos enviados están vacios
            String nit = txtNit.getText(), nombre = txtNombre.getText(), telefono = txtTelefono.getText(), correo = txtCorreo.getText();            
            if("".equals(nit)){
                throw new Exception("Ingrese el NIT, este campo es obligatorio");
            }
            if("".equals(nombre)){
                throw new Exception("Ingrese el nombre, este campo es obligatorio");
            }
            if("".equals(telefono)){
                telefono = "N/A";
            }
            if("".equals(correo)){
                correo = "N/A";
            }
            nuevoProveedor.setNitProveedor(nit);
            nuevoProveedor.setNombreProveedor(nombre);
            nuevoProveedor.setTelefonoProveedor(telefono);
            nuevoProveedor.setCorreoProveedor(correo);
            controladorProveedor.create(nuevoProveedor);
            iconoMostrar = vistaImg.iconoExcepcion("success");
            JOptionPane.showMessageDialog(null, "El proveedor ha sido creado con éxito", "MENSAJE DE INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);
            limpiar();
            llenarTablaProveedor();
            
        } catch (Exception e) {
            iconoMostrar = vistaImg.iconoExcepcion("error");
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);   
        }
        
    }//GEN-LAST:event_btnCrearActionPerformed

