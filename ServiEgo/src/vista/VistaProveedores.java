    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        iconoMostrar = vistaImg.iconoExcepcion("pregunta");
        int quiere = JOptionPane.showConfirmDialog(null, "¿Está seguro?", "ELIGE UNA OPCIÓN", 0, HEIGHT, iconoMostrar);
        switch (quiere) {
            case 0:
                try {
                    List<Producto> proveedorConProducto = productoControlador.preguntarProveedores(proveedorSeleccionado);
                    if(!proveedorConProducto.isEmpty()){
                        throw new Exception("El proveedor tiene asignado un producto y por seguridad no es posible eliminarlo");
                    }
                    controladorProveedor.destroy(proveedorSeleccionado.getCodigoProveedor());
                    iconoMostrar = vistaImg.iconoExcepcion("success");
                    JOptionPane.showMessageDialog(null, "El proveedor ha sido eliminado con éxito", "MENSAJE DE INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE, iconoMostrar);
                    limpiar();
                    llenarTablaProveedor();
                    
                } catch (Exception e) {
                    iconoMostrar = vistaImg.iconoExcepcion("error");
                    JOptionPane.showMessageDialog(null, e.getMessage(),"ERROR", JOptionPane.ERROR_MESSAGE, iconoMostrar);
                } break;
            case 1:
                //no
                break;
            default:
                break;
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtNitKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNitKeyTyped
        
        char c = evt.getKeyChar();

        if ((c < '0' || c > '9') & (c < '-' || c > '-')) {
            evt.consume();//esto es para que solo pueda ingresar números
        }
        
    }//GEN-LAST:event_txtNitKeyTyped

